/*
  MEMO: pcolor関数の仕様
  輝度Yと色RGBの対応表に紐ずく値を返却する。
  pcolor(何列目, 何色(何行目), どのテーブル);
  pcolor(Y, BGR(0 or 1 or 2), table);
*/

int pcolor(int ix, int ich, int map)
{
	int *pmap=NULL;

	switch (map) {
		case HSV:       pmap=pmap_hsv; break;
		case HOT:       pmap=pmap_hot; break;
		case GRAY:      pmap=pmap_gray; break;
		case BONE:      pmap=pmap_bone; break;
		case COPPER:    pmap=pmap_copper; break;
		case PINK:      pmap=pmap_pink; break;
		case WHITE:     pmap=pmap_white; break;
		case FLAG:      pmap=pmap_flag; break;
		case JET:       pmap=pmap_jet; break;
		case PRISM:     pmap=pmap_prism; break;
		case COOL:      pmap=pmap_cool; break;
		case LINES:     pmap=pmap_lines; break;
		case COLORCUBE: pmap=pmap_colorcube; break;
		case SUMMER:    pmap=pmap_summer; break;
		case AUTUMN:    pmap=pmap_autumn; break;
		case WINTER:    pmap=pmap_winter; break;
		case SPRING:    pmap=pmap_spring; break;
	}

	if(pmap) return pmap[3*ix+ich];
	return 0;
}